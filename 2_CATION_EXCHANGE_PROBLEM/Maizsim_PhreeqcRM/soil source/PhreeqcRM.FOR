      !   AUTHOR: ADITYA_KAPOOR
      !   THIS FILE CONTAINS TWO SUBROUTINES:
      !                1. TO INITIALIZE :  'SUBROUTINE PHREEQC_RM_INITIALIZE': SECOND IN ORDER IN THIS FILE
      !                2. FOR TIME STEPS:  'SUBROUTINE PHREEQC_RM'
      
      !   THIS VERSION IS FOR BENCHMARKING STUDY #2: CATION EXCHANGE STUDY DOCUMENTED IN https://pubs.usgs.gov/tm/06/a43/pdf/tm6-A43.pdf  EXAMPLE 
      !                1. A HYPOTHETICAL SOLUTE IS BEING INTRODUCED FROM THE TOP WITH A CONSTANT CONCENTRATION OF 1000 mg/l OR 1 mol/litre GIVEN THAT THE GRAM FORMULA WEIGHT (GFW =1 mol/gm)
      !                2. FERTIGATION_SCHEDULE.txt: CONTAINS SCHEDULE OF THE INTRODUCTION OF SOLUTE (AS DIRICHLET OR NEUMANN BC)
      !                3. OUTPUT FILE WITH CONCENTRATION OUTPUT FOR SOLUTE (ONE AND PRIMARY SOLUTE): FERTIGATION_OUTPUT.txt
      
      !   ASSOCIATED FILES AND SUBROTUINES:
      !                1. CORRESPONDING PHREEQC INPUT FILE: PHREEQCRM_RUNFILE_BENCHMARKING_STUDY_1.pqi
      !                2. OUTPUT FILE WITH THE CONCENTRATION OF OTHER SPECIES/SOLUTES: PHREEQC_SPECIES_OUT.txt
  
      !   NOTES:       1. THE STATUS FLAG SHOULD BE 0 AFTER A CALL. -VE VALUE MEANS ERROR
      !                2. KEEP THE LIBRARY 'PhreeqcRM.dll' (PhreeqcRM.dll RESULTS FROM RELEASE and PhreeqcRMd.dll IS DEBUG OUTPUT) AND THE DATABASE (Phreeqc.dat or Amm.dat) IN THE OUTPUT DIRECTORY
      !                       2.1 USE RELEASE VERSION THAT CREATES 'PhreeqcRM dll'
      !                3. EQUILIBRIUM REACTION ARE LIKE STEADY STATE REACTIONS AND IMPLY THE CONDITIONS AFTER A LONG TIME,
      !                   IF A USER IS LOOKING AT FINITE/CONTROLLED CHANGE, THEN USE KINETICS AND THE ASSOCIATED RATES 
      !                   FOR EXAMPLE: ALL THE NH3 MAY GET CONVERTED TO NH4+ AND OTHER N SPECIES
      !                4. THE RM_GetSpeciesConcentrations CAN ONLY RETURN THE CONCENTRATIONS IN mol/l. SO CHANGE IT TO mg/l using GRAM FORMULA WEIGHT (GFW)
      !                5. PHREEQCRM WORKS BEST IF THE VOLUME OF SOLUTION IS ONE TO TWO MAGNITUDES APART FROM A LITRE. IN CASE OF UNSATURATED SOILD OR SOILS WITH VERY
      !                   SMALL VOLUMES, THIS MAY NOT BE TRUE. SO A REPRESENTATIVE VOLUME OF 1 LITRE MAY BE IMPLEMENTED. THUS:
      !                       5.1 WHEN DEALING WITH EXTRINSIC PROPERTIES, SCALE APPROPRIATELY FOR APPROPRIATE UNITS
      !                6. THE FILE RM_interface.F90 CONTAINS LOT OF ADDITIONAL FUNCTIONS THAT ARE ABSENT IN THE DOCUMENTATION. REFER THOSE FOR BETTER UNDERSTANDING
      !                7. IN THE CONTEXT OF REDOX REACTIONS, PHREEQC PROVIDES pe AS SELECTED OUTPUT. IT HAS BEEN CONVERTED TO Eh (REDOX POTENTIAL)
      
      ! POSSIBLE CONFLICT: INITIALLY, CONCENTRATION ARE INGESTED FROM THE COMPONENTS LISTED IN THE PHREEQC INPUT FILE. THIS MAY OVERRIDE THE CONCENTRATIONS IN GRID AND BOUNDARY FILE
      
      !   REFERENCES : 1) https://phreeqcusers.org/index.php/topic,2620.0.html
      !                2) https://phreeqcusers.org/index.php?topic=1823.0      
      !                3) https://wwwbrr.cr.usgs.gov/projects/GWC_coupled/phreeqcrm/classphreeqcrm.html#a4cd19e7bf7d6dabe9424cfa559fab814
      !                4) https://github.com/usgs-coupled/phreeqcrm
      !                5) https://phreeqcusers.org/index.php?topic=1350.0 ! DISCUSSED THE UNITS OF CONCENTRATIONS FROM RM_GetSpeciesConcentrations 
      !                6) https://phreeqcusers.org/index.php?topic=2483.0 ! DISCUSSED ABOUT THE CHARGE BALANCE
      
      !   SOME IMPORTANT VARIABLES FROM 2DSOIL:
      !                1) NumSD: MAXIMUM NUMBER OF SOLUTES
      !                2) NumNPD: MAXIMUM NUMBER OF FEM MESH NODES
      !                3) NumNP: NUMBER OF FEM MESH NODES 
      
      !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
      !!!!!         SUBROUTINE TO CALL PHREEQC DURING TIME STEPS         !!!!!
      !!!!!                       USES RM_interface.F90                  !!!!!
      !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!      
      SUBROUTINE PHREEQC_RM
      USE PhreeqcRM
      INCLUDE 'public.ins'
      INCLUDE 'nitvar.ins'
      
                Common /WaterM/ ThOld(NumNPD),hTemp(NumNPD),hOld(NumNPD),
     !          ConAxx(NumElD),ConAzz(NumElD),ConAxz(NumElD),
     !          MaxIt,TolTh,TolH,dt,dtOld,tOld,
     !          thR(NMatD),hSat(NMatD),
     !          isat(NumBPD),FreeD
                      
                Common /GasM/ ThTot(NMatD)
                
                common/SPECIES_NUMBER/A_SPECIES_NUMBER,B_SPECIES_NUMBER,C_SPECIES_NUMBER,D_SPECIES_NUMBER
      
      INTEGER :: I,J,K,M, DUMMY_INT
      INTEGER :: AMM_COMP_NUMBER   ! TO IDENTIFY THE COMPONENT NUMBER OF AMM IN THE PHREEQC OUTPUT
      INTEGER :: A_SPECIES_NUMBER   ! TO IDENTIFY THE SPECIES NUMBER OF A IN THE PHREEQC OUTPUT
      INTEGER :: B_SPECIES_NUMBER   ! TO IDENTIFY THE SPECIES NUMBER OF B IN THE PHREEQC OUTPUT
      INTEGER :: C_SPECIES_NUMBER   ! TO IDENTIFY THE SPECIES NUMBER OF C IN THE PHREEQC OUTPUT
      INTEGER :: D_SPECIES_NUMBER   ! TO IDENTIFY THE SPECIES NUMBER OF D IN THE PHREEQC OUTPUT
      INTEGER :: Na_SPECIES_NUMBER   ! TO IDENTIFY THE SPECIES NUMBER OF Na (Sodium - Na+) IN THE PHREEQC OUTPUT
      INTEGER :: K_SPECIES_NUMBER   ! TO IDENTIFY THE SPECIES NUMBER OF K (Potassium - K+) IN THE PHREEQC OUTPUT
      INTEGER :: NO3_SPECIES_NUMBER   ! TO IDENTIFY THE SPECIES NUMBER OF NO3 (Nitrate - NO3-) IN THE PHREEQC OUTPUT
      INTEGER :: Ca_SPECIES_NUMBER   ! TO IDENTIFY THE SPECIES NUMBER OF Ca (Calcium - Ca2+) IN THE PHREEQC OUTPUT
      INTEGER :: Cl_SPECIES_NUMBER   ! TO IDENTIFY THE SPECIES NUMBER OF Cl (Chloride - Cl-) IN THE PHREEQC OUTPUT
      INTEGER :: AMMH_SPECIES_NUMBER   ! TO IDENTIFY THE SPECIES NUMBER OF AMMH+ IN THE PHREEQC OUTPUT
      INTEGER :: H2O_SPECIES_NUMBER   ! TO IDENTIFY THE SPECIES NUMBER OF H2O IN THE PHREEQC OUTPUT
      INTEGER :: pe_OUT_NUMBER   ! TO IDENTIFY THE NUMBER FOR pe IN THE PHREEQC SELECT OUTPUT
      INTEGER :: SELECT_OUTPUT_COUNT
      INTEGER :: SELECTED_OUTPUT_USER_NUMBER
      INTEGER :: SELECTED_OUTPUT_COLUMN_COUNT
      INTEGER :: SELECTED_OUTPUT_ROW_COUNT
      INTEGER :: PHREEQCRM_ID,STATUS
      INTEGER :: N_COMPS, N_SPECIES
      INTEGER :: CHEMISTRY_CELL_COUNT ! TO CHECK THE NUMBER OF ACTIVE REACTION CELLS IN PHREEQCRM
      
      INTEGER, DIMENSION(:), ALLOCATABLE :: SOLN_IC,  ! SWITCH FOR SOLUTION BASED INITIAL CONDITION 
     -                                      EQBM_IC,  ! SWITCH FOR EQUILIBRIUM BASED INITIAL CONDITION 
     -                                      EXCH_IC,  ! SWITCH FOR EXCHANGE BASED INITIAL CONDITION 
     -                                      SURF_IC,  ! SWITCH FOR SURFACE BASED INITIAL CONDITION 
     -                                      GAS_IC,   ! SWITCH FOR GAS PHASE BASED INITIAL CONDITION 
     -                                      SOLID_IC, ! SWITCH FOR SOLIDS BASED INITIAL CONDITION 
     -                                      KNTC_IC   ! SWITCH FOR KINETICS BASED INITIAL CONDITION 
      
      CHARACTER (LEN = 100) :: DUMMY_CHAR
      CHARACTER(LEN = :), DIMENSION(:), ALLOCATABLE :: NAME_COMPS   ! NAME OF COMPONENTS
      CHARACTER(LEN = :), DIMENSION(:), ALLOCATABLE :: NAME_SPECIES   ! NAME OF SPECIES
      CHARACTER(LEN = :), DIMENSION(:), ALLOCATABLE :: SEL_OUT_COL_HEADINGS
      DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: GFW_COMPS      ! GRAM FORMULA WEIGHTS OF COMPONENTS
      DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: SOLUTION_VOLUME_OUT      ! TO FETCH THE OUTPUT OF SOLUTION VOLUME FOMR PHREEQCRM
      DOUBLE PRECISION, DIMENSION(:,:), ALLOCATABLE :: CONC_COMPS     ! NODE WISE CONCENTRATION OF ALL COMPONENTS
      DOUBLE PRECISION, DIMENSION(:,:), ALLOCATABLE :: CONC_SPECIES     ! NODE WISE CONCENTRATION OF ALL SPECIES
      DOUBLE PRECISION, DIMENSION(:,:), ALLOCATABLE :: SEL_OUT_CONC     ! CONCENTRATION OF SELECTED OUTPUTS
      DOUBLE PRECISION, DIMENSION(:,:), ALLOCATABLE :: SEL_OUT_ARRAY     ! CONCENTRATION OF SELECTED OUTPUTS
      DOUBLE PRECISION, DIMENSION(:,:), ALLOCATABLE :: BC_CONC     ! NODE WISE CONCENTRATION OF COMPONENTS
      INTEGER, DIMENSION(:), ALLOCATABLE :: BC_CONC_MIX     ! NODE WISE CONCENTRATION OF COMPONENTS OF BC TO BE MIXED WITH BC_CONC
      INTEGER, DIMENSION(:), ALLOCATABLE :: BC_MIX_RATIO     ! NODE WISE MIXING RATIO OF COMPONENTS OF BC TO BE MIXED WITH BC_CONC
      DOUBLE PRECISION :: TIME_PHREEQ,TIME_STEP_PHREEQ    ! TIME AND TIME STEP FOR PHREEQCRM: BY DEFAULT THESE SHOULD BE IN SECONDS
      SAVE TIME_PHREEQ
      REAL:: GFW_NH4 = 18.04  ! GRAM FORMULA WEIGHT (GFW) OR MOLAR MASS FOR NH4 IN GRAMS
      REAL:: GFW_Na = 22.9898  ! GRAM FORMULA WEIGHT (GFW) OR MOLAR MASS FOR Na IN GRAMS
      REAL:: GFW_K = 39.102  ! GRAM FORMULA WEIGHT (GFW) OR MOLAR MASS FOR K IN GRAMS
      REAL:: GFW_Ca = 40.08  ! GRAM FORMULA WEIGHT (GFW) OR MOLAR MASS FOR Ca IN GRAMS
      REAL:: GFW_Cl = 35.453  ! GRAM FORMULA WEIGHT (GFW) OR MOLAR MASS FOR Cl IN GRAMS
      REAL:: GFW_NO3 = 62.01  ! GRAM FORMULA WEIGHT (GFW) OR MOLAR MASS FOR NO3- IN GRAMS
      REAL:: GFW_A = 1.00  ! GRAM FORMULA WEIGHT (GFW) OR MOLAR MASS FOR A IN GRAMS
      REAL:: GFW_B = 1.00  ! GRAM FORMULA WEIGHT (GFW) OR MOLAR MASS FOR B IN GRAMS
      REAL:: GFW_C = 1.00  ! GRAM FORMULA WEIGHT (GFW) OR MOLAR MASS FOR C IN GRAMS
      REAL:: GFW_D = 1.00  ! GRAM FORMULA WEIGHT (GFW) OR MOLAR MASS FOR D IN GRAMS
      
      DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: REP_VOL ! REPRESENTATIVE VOLUME : KEEP IT IN THE MAGNITUDE OF 0.1 TO 10 LITRES i.e. ONE TO TWO ORDER OF MAGNITUDES AWAY FROM A LITRE
      DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: REP_VOL_1_L ! REPRESENTATIVE VOLUME IS SET TO ONE LITRE OT PREVENT CONVERGENCE ISSUES
      DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: POROSITY ! POROSITY
      DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: SATURATION ! SATURATION: VOLUME OF PORES FILLED WITH WATER / VOLUME OF PORES
      DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: TEMP ! TEMPERATURE ARRAY
      DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: DENSITY  ! IF DENSITY DEPENDENT FLOWS ARE IMPLEMENTED IN THE FUTURE
      DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: PRESSURE  ! PRESSURE = HEAD*DENSITY*ACC_DUE_TO_GRAVITY        (H*pho*g)
      DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: VISCOSITY  ! PRESSURE = HEAD*DENSITY*ACC_DUE_TO_GRAVITY        (H*pho*g)
      
      DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: AMM_CONC     ! NODE WISE CONCENTRATION OF AMM (ONE SPECIES: AMMH+)
      DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: Eh     ! REDOX POTENTIAL
      
      ALLOCATE(REP_VOL(NUMNP))
      ALLOCATE(REP_VOL_1_L(NUMNP))
      ALLOCATE(POROSITY(NUMNP))
      ALLOCATE(SATURATION(NUMNP))
      ALLOCATE(TEMP(NUMNP))
      ALLOCATE(DENSITY(NUMNP))
      ALLOCATE(PRESSURE(NUMNP))
      ALLOCATE(VISCOSITY(NUMNP))
      ALLOCATE(SOLUTION_VOLUME_OUT(NUMNP))
      
      ALLOCATE(AMM_CONC(NUMNP))
      ALLOCATE(Eh(NUMNP))
      
      !!!!!       INITIALIZE PHREEQCRM      !!!!!
      IF (lInput == 1) THEN       ! lInput IS 1 DURING INITIALIZATION GLOBALLY
      CALL PHREEQC_RM_INITIALIZE()      
             
      STATUS = RM_SetSpeciesSaveOn(PHREEQCRM_ID, 1)   !   A PREREQUISITE FOR GET SPECIES CONCENTRATIONS
      N_COMPS = RM_FindComponents(PHREEQCRM_ID)
      STATUS = RM_GetGfw(PHREEQCRM_ID,GFW_COMPS)
      STATUS = RM_GetComponents(PHREEQCRM_ID,NAME_COMPS)
      N_SPECIES = RM_GetSpeciesCount(PHREEQCRM_ID)
      STATUS = RM_GetSpeciesNames(PHREEQCRM_ID,NAME_SPECIES)
      
      !   GETTING THE INFORMATION REGARDING COMPONENTS FORMED DURING INITIAL PHREEQCRM RUN    !!!!!
      WRITE(*,*) "N_COMPONENTS = ",  N_COMPS
      WRITE(*,*) "N_SPECIES = ",  N_SPECIES
      
!      ALLOCATE(GFW_COMPS(N_COMPS))   ! PHREEQCRM ALLOCATES THE ARRAY FOR THE USER
!      ALLOCATE(NAME_COMPS(N_COMPS))   ! PHREEQCRM ALLOCATES THE ARRAY FOR THE USER
      
      !!!!!         DEFINE INITIAL CONDITIONS        !!!!!
      !   1 FOR ACTIVATION, -1 FOR DEACTIVATION   
      ALLOCATE(SOLN_IC(NUMNP))
      ALLOCATE(KNTC_IC(NUMNP))
      ALLOCATE(EQBM_IC(NUMNP))
      ALLOCATE(EXCH_IC(NUMNP))
      SOLN_IC =  0     ! 0th SOLUTION RELATED INITIAL CONDITION IS RUN, -1: NOT RUN
      KNTC_IC =  -1     ! 0th KINETICS RELATED INITIAL CONDITION IS RUN, -1: NOT RUN
      EQBM_IC = 0
      EXCH_IC = 0
      STATUS = RM_InitialSolutions2Module(PHREEQCRM_ID,SOLN_IC)
      STATUS = RM_InitialKinetics2Module(PHREEQCRM_ID,KNTC_IC)   ! UNCOMMENT IF KINETICS IS ACTIVATED
      STATUS = RM_InitialExchanges2Module(PHREEQCRM_ID,EXCH_IC)
!      STATUS = RM_InitialEquilibriumPhases2Module(PHREEQCRM_ID, EQBM_IC)
      
      !!!!!       INITIAL EQUILIBRIUM OF CELLS        !!!!!
      TIME_PHREEQ = 0.0
      TIME_STEP_PHREEQ = 0.0

      STATUS = RM_SetTime(PHREEQCRM_ID,TIME_PHREEQ)   ! INSTRUMENTAL IN KINETICS WITH RATES DEFINED
      STATUS = RM_SetTimeStep(PHREEQCRM_ID,TIME_STEP_PHREEQ)
      STATUS = RM_SetSelectedOutputOn(PHREEQCRM_ID,1)
      STATUS = RM_RunFile(PHREEQCRM_ID,1,0,0,"PHREEQCRM_RUNFILE_CATION_EXCHANGE.pqi")
      STATUS = RM_RunCells(PHREEQCRM_ID)
      CHEMISTRY_CELL_COUNT = RM_GetChemistryCellCount(PHREEQCRM_ID)
      
      IF (CHEMISTRY_CELL_COUNT.NE.NUMNP) THEN
          WRITE(*,*)  "WARNING: MISMATCH BETWEEN NUMBER OF FEM NODES AND PHREEQCRM REACTION CELLS. CHECK INPUT FILES"
      END IF
      
      WRITE (*,*) "INITIAL CELLS RUN"
      
      !   GET COMPONENT AND SPECIES CONCENTRATIONS FOR THIS TIME STEP
      STATUS = RM_GetConcentrations(PHREEQCRM_ID,CONC_COMPS)  ! RETURNS CONCENTRATIONS IN mg/l
      STATUS = RM_GetSpeciesConcentrations(PHREEQCRM_ID,CONC_SPECIES) ! RETURNS CONCENTRATIONS IN mol/l
      
      ! WRITING BLOCK FOR NODE#1
      WRITE(*,*) "DURING TIME STEPPING"      
      WRITE (*,*) "ENUMERATING COMPONENTS:"
      DO I = 1,N_COMPS
          WRITE(*,*) I,NAME_COMPS(I),CONC_COMPS(1,I)
      END DO
      WRITE (*,*) ""
      WRITE (*,*) "ENUMERATING SPECIES:"
      DO I = 1,N_SPECIES
          WRITE(*,*) I,NAME_SPECIES(I),CONC_SPECIES(1,I)
      END DO
      
      A_SPECIES_NUMBER = 0
      B_SPECIES_NUMBER = 0
      C_SPECIES_NUMBER = 0
      D_SPECIES_NUMBER = 0
      Na_SPECIES_NUMBER = 0
      K_SPECIES_NUMBER = 0
      Ca_SPECIES_NUMBER = 0
      Cl_SPECIES_NUMBER = 0
      NO3_SPECIES_NUMBER = 0
      
      
      DO I = 1, N_SPECIES      
          IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == '[A]') THEN
              A_SPECIES_NUMBER = I
          ELSE IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == '[B]') THEN
              B_SPECIES_NUMBER = I
          ELSE IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == '[C]') THEN
              C_SPECIES_NUMBER = I
          ELSE IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == 'Na+') THEN
              Na_SPECIES_NUMBER = I              
          ELSE IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == 'K+') THEN
              K_SPECIES_NUMBER = I     
          ELSE IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == 'Ca+2') THEN
              Ca_SPECIES_NUMBER = I  
          ELSE IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == 'Cl-') THEN
              Cl_SPECIES_NUMBER = I
          ELSE IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == 'NO3-') THEN
              NO3_SPECIES_NUMBER = I
          END IF
      END DO
      
      WRITE (*,*) "Na_SPECIES_NUMBER = ", Na_SPECIES_NUMBER
      WRITE (*,*) "K_SPECIES_NUMBER = ", K_SPECIES_NUMBER
      WRITE (*,*) "Ca_SPECIES_NUMBER = ", Ca_SPECIES_NUMBER
      WRITE (*,*) "Cl_SPECIES_NUMBER = ", Cl_SPECIES_NUMBER
      WRITE (*,*) "NO3_SPECIES_NUMBER = ", NO3_SPECIES_NUMBER
      
      
      !   ENUMERATING SELECTED OUTPUT
      SELECTED_OUTPUT_USER_NUMBER = 1
!      WRITE (*,*) "SELECTED_OUTPUT_USER_NUMBER",SELECTED_OUTPUT_USER_NUMBER
      STATUS = RM_SetCurrentSelectedOutputUserNumber(PHREEQCRM_ID,SELECTED_OUTPUT_USER_NUMBER)
      SELECT_OUTPUT_COUNT = RM_GetSelectedOutputcount(PHREEQCRM_ID)
      SELECTED_OUTPUT_COLUMN_COUNT = RM_GetSelectedOutputcolumncount(PHREEQCRM_ID)
      SELECTED_OUTPUT_ROW_COUNT = RM_GetSelectedOutputrowcount(PHREEQCRM_ID)
      ! SEL_OUT_ARRAY CONTAINS SELECTED OUTPUT (COLUMNS SUCH AS pH, pe ETC.) FOR ALL NODES (ROWS). HENCE THE DIMENSIONS: ROWS*COLUMNS
      ALLOCATE(SEL_OUT_ARRAY(NUMNP,SELECTED_OUTPUT_COLUMN_COUNT))
      STATUS = RM_GetSelectedOutput(PHREEQCRM_ID,SEL_OUT_ARRAY)
      STATUS = RM_GetSelectedOutputheadings(PHREEQCRM_ID,SEL_OUT_COL_HEADINGS)
      WRITE (*,*) ""
      
      !   THIS BLOCK CAN BE COMMENTED OUT OF THE CONCENTRATIONS FROM GRID AND BOUNDARY FILE NEEDS TO BE RETAINED 
      !   ASSIGN THE UPDATED CONCENTRATIONS HERE
      !   SET CONCENTRATION FOR PASSING INTO PHREEQCRM      
      DO I = 1,NUMNP
          Conc(I,1) = CONC_SPECIES(I,Ca_SPECIES_NUMBER)*(GFW_Ca*1000)     ! GETTING Conc IN mg/l FROM CONC_SPECIES IS IN mol/l 
          Conc(I,2) = CONC_SPECIES(I,Cl_SPECIES_NUMBER)*(GFW_Cl*1000)     ! GETTING Conc IN mg/l FROM CONC_SPECIES IS IN mol/l 
          Conc(I,3) = CONC_SPECIES(I,Na_SPECIES_NUMBER)*(GFW_Na*1000)     ! GETTING Conc IN mg/l FROM CONC_SPECIES IS IN mol/l 
          Conc(I,4) = CONC_SPECIES(I,K_SPECIES_NUMBER)*(GFW_K*1000)       ! GETTING Conc IN mg/l FROM CONC_SPECIES IS IN mol/l 
          Conc(I,5) = CONC_SPECIES(I,NO3_SPECIES_NUMBER)*(GFW_NO3*1000)   ! GETTING Conc IN mg/l FROM CONC_SPECIES IS IN mol/l 
      END DO
      
      WRITE (*,*) "ENUMERATING SELECTED OUTPUT:"
      
      DO I = 1, SELECTED_OUTPUT_COLUMN_COUNT
          WRITE(*,*) TRIM(SEL_OUT_COL_HEADINGS(I)),'  ',SEL_OUT_ARRAY(1,I)
      END DO
      WRITE (*,*) ""
      
      DO I = 1, SELECTED_OUTPUT_COLUMN_COUNT
          IF (TRIM(ADJUSTL(SEL_OUT_COL_HEADINGS(I))) == 'pe') THEN
          pe_OUT_NUMBER = I
          WRITE(*,*) " pe_OUT_NUMBER = ", pe_OUT_NUMBER
          END IF
      END DO

      
      !!!!!   pe to Eh (IN MILLI VOLTS) CONVERSION     !!!!!
      !   Eh = 1000*pe*(2.303*R*T)/F      ! 1000 FOR CONVERSION FROM VOLTS TO MILLIVOLTS
      !   pe : POWER OF ELECTRONS
      !   Eh : REDOX POTENTIAL IN VOLTS
      !   R  : IDEAL GAS CONSTANT (8.3144 J/K mol)
      !   T  : ABSOLOUTE TEMPERATURE (IN KELVIN)
      !   F  : FARADAY CONSTANT (96484 C/mol = J/V mol)      
      
      DO I = 1, NUMNP
          Eh(I) = 1000*(SEL_OUT_ARRAY(I,pe_OUT_NUMBER))*(2.303*8.3144*(TEMP(I)+273.15))/(96484)
      END DO
      !
      !DO I = 1,NUMNP
      !    CONC_SPECIES(I,H2O_SPECIES_NUMBER) = 55.55
      !END DO

      STATUS = RM_SpeciesConcentrations2Module(PHREEQCRM_ID,CONC_SPECIES)
      
      ! REDUNDANT BLOCK BUT ADDED DUE TO THE EXAMPLE FILE STRUCTURE
      !!!!! SET BOUNDARY CONDITIONS
      ALLOCATE(BC_CONC(NumBP,N_COMPS))
      ALLOCATE(BC_CONC_MIX(NumBP))
      ALLOCATE(BC_MIX_RATIO(NumBP))

      BC_CONC = 0     ! SOLUTION#0 FROM INTIAL PHREEQC INSTANCE 
      BC_CONC_MIX = -1    ! NO BC MIXING
      BC_MIX_RATIO = 1    ! BC_CONC FRACTION IS 1 AND FOR BC_CONC_MIX IS 0
      !STATUS = RM_InitialPhreeqc2Concentrations(PHREEQCRM_ID,BC_CONC,NumBP,BC_CONC_MIX,BC_MIX_RATIO)      
      !STATUS = RM_InitialPhreeqc2SpeciesConcentrations(PHREEQCRM_ID,BC_CONC,NumBP,BC_CONC_MIX,BC_MIX_RATIO) 
      
      ! WRITING THE OUTPUT FOR SELCTIVE SPECIES TO A FILE
      OPEN(UNIT = 1001, FILE = 'PHREEQC_SPECIES_OUT.txt', STATUS = 'UNKNOWN')
      WRITE(1001,*) 'NODE_NUM','  ','TIME','  ','DATE','  ',' ','X',' ','Y',' ','Ca_CONC','     ','Cl_CONC',
     -'     ','Na_CONC','    ','K_CONC'
      
      DO I = 1, NUMNP
          WRITE (1001,'(I4,1X,F14.2,1X,A10,1X,F10.2,1X,F10.2,1X,F10.4,1X,F10.4,1X,F10.4,1X,F10.4)') 
     - I,TIME_PHREEQ, Date,x(I),y(I),Conc(I,1),Conc(I,2),Conc(I,3),Conc(I,4),Conc(I,4) 
      END DO
      
      !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
      !!!!!       INITIALIZE TIMESTEPPING   !!!!!          
      ELSE IF (lInput == 0) THEN  ! lInput IS 0 POST INITIALIZATION      
      TIME_STEP_PHREEQ = Step*86400  ! TO CONVERT FROM HOURS TO SECONDS FOR PHREEQCRM
      TIME_PHREEQ = TIME_PHREEQ + TIME_STEP_PHREEQ  ! TIME ELAPSED IN SECONDS
      WRITE(*,*) " TIME_STEP = ", TIME_STEP_PHREEQ, " TIME  = ", TIME_PHREEQ
      
      STATUS = RM_SetTime(PHREEQCRM_ID,TIME_PHREEQ)
      STATUS = RM_SetTimeStep(PHREEQCRM_ID,TIME_STEP_PHREEQ)
      write(*,*) 'STEP = ', step
      !   SET PRINTING CONDITIONS
!      STATUS = RM_SetOutputFileOn(PHREEQCRM_ID,1)
      STATUS = RM_SetSelectedOutputOn(PHREEQCRM_ID,1)
      STATUS = RM_SetPrintChemistryOn(PHREEQCRM_ID, 1, 0, 0) ! ORDER: WORKERS, INITIAL PHREEQCRM, UTILITY
      
      DO I = 1, NUMNP
          SATURATION(I) = ThNew(I)
          M = MatNumN(I)
          POROSITY(I) = ThTot(M)
          REP_VOL(I) = (nodeArea(I)*1.0)*0.001   ! ADOPTING 1 cm WIDTH AND SUBSEQUENT CONVERTED TO LITRES. WARNING: REP_VOL WITHIN 1 TO 2 MAGNITUDES OF A LITRE
          REP_VOL_1_L(I) = 1.0   ! SETS REPRESENTATIVE VOLUME TO ONE LITRE TO PREVENT CONVERGENCE ISSUES
          TEMP(I) = Tmpr(I)    ! IN DEGREE CELSIUS BY DEFAULT
          DENSITY(I) = 1.0  ! IN Kg/Litre
          PRESSURE(I) = (hNew(I)*DENSITY(I)*9.81)/(101325) ! IN ATMOSPHERE (atm): 1 atm = 101325 Pascals:    THE PRESSURE COMPUTED BY H*Pho*g IS IN Pa 
!         WRITE(*,*) "I =", I , "SATURATION = ", SATURATION(I),
!     -    "NH4 = ",Conc(I,1),"POROSITY = ", POROSITY(I)
      END DO
      
      !   SET SATURATION, POROSITY ,REP_VOL AND TEMPERATURE : PASSES ARRAYS TO PHREEQCRM
      STATUS = RM_SetSaturationUser(PHREEQCRM_ID, SATURATION)     ! SHOULD BE PLACED BEFORE RM_GetSpeciesConcentrations
      STATUS = RM_SetPorosity(PHREEQCRM_ID,POROSITY)
      STATUS = RM_SetRepresentativeVolume(PHREEQCRM_ID,REP_VOL)
      STATUS = RM_SetTemperature(PHREEQCRM_ID,TEMP)
      STATUS = RM_SetDensityUser(PHREEQCRM_ID,DENSITY)	
      STATUS = RM_SetPressure(PHREEQCRM_ID,PRESSURE)
!      WRITE(*,*) "STATUS  = " ,STATUS
      
      STATUS = RM_GetConcentrations(PHREEQCRM_ID,CONC_COMPS)  ! RETURNS CONCENTRATIONS IN mg/l
      STATUS = RM_GetSpeciesConcentrations(PHREEQCRM_ID,CONC_SPECIES) ! RETURNS CONCENTRATIONS IN mol/l
      

      
      !   GET COMPONENTS FOR THIS TIME STEP
      STATUS = RM_SetSpeciesSaveOn(PHREEQCRM_ID, 1)   !   A PREREQUISITE FOR GET SPECIES CONCENTRATIONS
      N_COMPS = RM_FindComponents(PHREEQCRM_ID)
      STATUS = RM_GetGfw(PHREEQCRM_ID,GFW_COMPS)  ! GFW_COMPS IS ALLOCATED AUTOMATICALLY BY PHREEQCRM
      STATUS = RM_GetComponents(PHREEQCRM_ID,NAME_COMPS)  ! NAME_COMPS IS ALLOCATED AUTOMATICALLY BY PHREEQCRM
      N_SPECIES = RM_GetSpeciesCount(PHREEQCRM_ID)
      STATUS = RM_GetSpeciesNames(PHREEQCRM_ID,NAME_SPECIES)
      STATUS = RM_GetViscosity(PHREEQCRM_ID,VISCOSITY)
      
      DO I = 1, N_SPECIES      
          IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == '[A]') THEN
              A_SPECIES_NUMBER = I
          ELSE IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == '[B]') THEN
              B_SPECIES_NUMBER = I
          ELSE IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == '[C]') THEN
              C_SPECIES_NUMBER = I
          ELSE IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == 'Na+') THEN
              Na_SPECIES_NUMBER = I              
          ELSE IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == 'K+') THEN
              K_SPECIES_NUMBER = I     
          ELSE IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == 'Ca+2') THEN
              Ca_SPECIES_NUMBER = I  
          ELSE IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == 'Cl-') THEN
              Cl_SPECIES_NUMBER = I
          ELSE IF (TRIM(ADJUSTL(NAME_SPECIES(I))) == 'NO3-') THEN
              NO3_SPECIES_NUMBER = I
          END IF
      END DO
      
      WRITE (*,*) "Na_SPECIES_NUMBER = ", Na_SPECIES_NUMBER
      WRITE (*,*) "K_SPECIES_NUMBER = ", K_SPECIES_NUMBER
      WRITE (*,*) "Ca_SPECIES_NUMBER = ", Ca_SPECIES_NUMBER
      WRITE (*,*) "Cl_SPECIES_NUMBER = ", Cl_SPECIES_NUMBER
      WRITE (*,*) "NO3_SPECIES_NUMBER = ", NO3_SPECIES_NUMBER
      
      !   ASSIGN THE UPDATED CONCENTRATIONS HERE
      !   SET CONCENTRATIONS FOR PASSING INTO PHREEQCRM      
      DO I = 1,NUMNP
          CONC_SPECIES(I,Ca_SPECIES_NUMBER) = Conc(I,1)/(GFW_Ca*1000)     ! GETTING CONC_SPECIES IN mol/l FROM Conc IN mg/l
          CONC_SPECIES(I,Cl_SPECIES_NUMBER) = Conc(I,2)/(GFW_Cl*1000)     ! GETTING CONC_SPECIES IN mol/l FROM Conc IN mg/l
          CONC_SPECIES(I,Na_SPECIES_NUMBER) = Conc(I,3)/(GFW_Na*1000)     ! GETTING CONC_SPECIES IN mol/l FROM Conc IN mg/l
          CONC_SPECIES(I,K_SPECIES_NUMBER) = Conc(I,4)/(GFW_K*1000)       ! GETTING CONC_SPECIES IN mol/l FROM Conc IN mg/l
          CONC_SPECIES(I,NO3_SPECIES_NUMBER) = Conc(I,5)/(GFW_NO3*1000)   ! GETTING CONC_SPECIES IN mol/l FROM Conc IN mg/l
      END DO
      
      STATUS = RM_SpeciesConcentrations2Module(PHREEQCRM_ID,CONC_SPECIES)
      
      !!!!!       RUN CELLS FOR THIS TIME STEPS
      STATUS = RM_RunCells(PHREEQCRM_ID)
      

      
      !   GET PROPERTIES FOR THIS TIME STEP
      STATUS = RM_GetDensityCalculated(PHREEQCRM_ID,DENSITY)
      STATUS = RM_GetSolutionVolume(PHREEQCRM_ID,SOLUTION_VOLUME_OUT) ! SOLUTION COLUME = REP VOL * POROSITY * SATURATION
      STATUS = RM_GetSaturationCalculated(PHREEQCRM_ID,SATURATION)      

        
!      ALLOCATE(CONC_COMPS(NUMNP,N_COMPS))
!      ALLOCATE(CONC_SPECIES(NUMNP,N_SPECIES))
      
      !   GET COMPONENT AND SPECIES CONCENTRATIONS FOR THIS TIME STEP
      STATUS = RM_GetConcentrations(PHREEQCRM_ID,CONC_COMPS)
      STATUS = RM_GetSpeciesConcentrations(PHREEQCRM_ID,CONC_SPECIES)
      
      ! NNH4(I)/(1000*GFW_NH4)      
      DO I = 1,NUMNP
          Conc(I,1) = CONC_SPECIES(I,Ca_SPECIES_NUMBER)*(GFW_Ca*1000)     ! GETTING Conc IN mg/l FROM CONC_SPECIES IS IN mol/l. Conc in mg/l = 1000*GFW*mol/l
          Conc(I,2) = CONC_SPECIES(I,Cl_SPECIES_NUMBER)*(GFW_Cl*1000)     ! GETTING Conc IN mg/l FROM CONC_SPECIES IS IN mol/l. Conc in mg/l = 1000*GFW*mol/l
          Conc(I,3) = CONC_SPECIES(I,Na_SPECIES_NUMBER)*(GFW_Na*1000)     ! GETTING Conc IN mg/l FROM CONC_SPECIES IS IN mol/l. Conc in mg/l = 1000*GFW*mol/l
          Conc(I,4) = CONC_SPECIES(I,K_SPECIES_NUMBER)*(GFW_K*1000)       ! GETTING Conc IN mg/l FROM CONC_SPECIES IS IN mol/l. Conc in mg/l = 1000*GFW*mol/l
          Conc(I,5) = CONC_SPECIES(I,NO3_SPECIES_NUMBER)*(GFW_NO3*1000)   ! GETTING Conc IN mg/l FROM CONC_SPECIES IS IN mol/l. Conc in mg/l = 1000*GFW*mol/l
      END DO
      
      !DO I = 1,NUMNP
      !    CONC_SPECIES(I,H2O_SPECIES_NUMBER) = 55.55
      !END DO
       STATUS = RM_SpeciesConcentrations2Module(PHREEQCRM_ID,CONC_SPECIES)
      
      ! WRITING BLOCK FOR NODE#1
      WRITE(*,*) "DURING TIME STEPPING"      
      WRITE (*,*) "ENUMERATING COMPONENTS:"
      DO I = 1,N_COMPS
          WRITE(*,*) I,NAME_COMPS(I),CONC_COMPS(1,I)
      END DO
      WRITE (*,*) ""
      WRITE (*,*) "ENUMERATING SPECIES:"
      DO I = 1,N_SPECIES
          WRITE(*,*) I,NAME_SPECIES(I),CONC_SPECIES(1,I)
      END DO
      !   ENUMERATING SELECTED OUTPUT
      SELECTED_OUTPUT_USER_NUMBER = 1
!      WRITE (*,*) "SELECTED_OUTPUT_USER_NUMBER",SELECTED_OUTPUT_USER_NUMBER
      STATUS = RM_SetCurrentSelectedOutputUserNumber(PHREEQCRM_ID,SELECTED_OUTPUT_USER_NUMBER)
      SELECT_OUTPUT_COUNT = RM_GetSelectedOutputcount(PHREEQCRM_ID)
      SELECTED_OUTPUT_COLUMN_COUNT = RM_GetSelectedOutputcolumncount(PHREEQCRM_ID)
      SELECTED_OUTPUT_ROW_COUNT = RM_GetSelectedOutputrowcount(PHREEQCRM_ID)
      ! SEL_OUT_ARRAY CONTAINS SELECTED OUTPUT (COLUMNS SUCH AS pH, pe ETC.) FOR ALL NODES (ROWS). HENCE THE DIMENSIONS: ROWS*COLUMNS
      ALLOCATE(SEL_OUT_ARRAY(NUMNP,SELECTED_OUTPUT_COLUMN_COUNT))
      STATUS = RM_GetSelectedOutput(PHREEQCRM_ID,SEL_OUT_ARRAY)
      STATUS = RM_GetSelectedOutputheadings(PHREEQCRM_ID,SEL_OUT_COL_HEADINGS)
      WRITE (*,*) ""
      WRITE (*,*) "ENUMERATING SELECTED OUTPUT:"
      DO I = 1, SELECTED_OUTPUT_COLUMN_COUNT
          WRITE(*,*) TRIM(SEL_OUT_COL_HEADINGS(I)),'  ',SEL_OUT_ARRAY(1,I)
      END DO
      WRITE (*,*) ""
      
      DO I = 1, SELECTED_OUTPUT_COLUMN_COUNT
          IF (TRIM(ADJUSTL(SEL_OUT_COL_HEADINGS(I))) == 'pe') THEN
          pe_OUT_NUMBER = I
          WRITE(*,*) " pe_OUT_NUMBER = ", pe_OUT_NUMBER
          END IF
      END DO
      
      
      WRITE(*,*) "CONCS:",Conc(NUMNP,1),Conc(NUMNP,2),Conc(NUMNP,3),Conc(NUMNP,4),Conc(NUMNP,5)
      
      !!!!!   pe to Eh (IN MILLI VOLTS) CONVERSION     !!!!!
      !   Eh = 1000*pe*(2.303*R*T)/F      ! 1000 FOR CONVERSION FROM VOLTS TO MILLIVOLTS
      !   pe : POWER OF ELECTRONS
      !   Eh : REDOX POTENTIAL IN VOLTS
      !   R  : IDEAL GAS CONSTANT (8.3144 J/K mol)
      !   T  : ABSOLOUTE TEMPERATURE (IN KELVIN)
      !   F  : FARADAY CONSTANT (96484 C/mol = J/V mol)      
      
      DO I = 1, NUMNP
          Eh(I) = 1000*(SEL_OUT_ARRAY(I,pe_OUT_NUMBER))*(2.303*8.3144*(TEMP(I)+273.15))/(96484)
      END DO
      
      iday=int(time)
      call caldat(iday,mm,id,iyyy) 
      
      IF (MOD(INT(TIME_PHREEQ),86400) == 0) THEN
      DO I = 1, NUMNP
          WRITE (1001,'(I4,1X,F14.2,1X,I2.2,A1,I2.2,A1,I4,1X,F10.2,1X,F10.2,1X,F10.4,1X,F10.4,1X,F10.4,1X,F10.4)') 
     - I,TIME_PHREEQ, mm,'/',id,'/',iyyy,x(I),y(I),Conc(I,1),Conc(I,2),Conc(I,3),Conc(I,4),Conc(I,5)
      END DO
      END IF
      
      END IF  ! END OF IF STATEMENT GOVERNING THE INITIALIZATION AND TIME STEPPING
      
!      PAUSE           
      
      RETURN
      END SUBROUTINE PHREEQC_RM

            
      !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
      !!!!!         SUBROUTINE TO INITIALIZE PHREEQCRM          !!!!!
      !!!!!               USES RM_interface.F90                 !!!!!
      !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!      
      SUBROUTINE PHREEQC_RM_INITIALIZE
          USE PhreeqcRM
          INCLUDE 'public.ins'
          INCLUDE 'nitvar.ins'
          
                Common /WaterM/ ThOld(NumNPD),hTemp(NumNPD),hOld(NumNPD),
     !                ConAxx(NumElD),ConAzz(NumElD),ConAxz(NumElD),
     !                MaxIt,TolTh,TolH,dt,dtOld,tOld,
     !                thR(NMatD),hSat(NMatD),
     !                isat(NumBPD),FreeD
                                
                Common /GasM/ ThTot(NMatD)
      
          INTEGER:: I, J, K, M
          INTEGER:: N_THREADS
          INTEGER:: PHREEQCRM_ID
          INTEGER:: STATUS
          DOUBLE PRECISION :: LB_FACTOR   ! LOAD BALANCING FACTOR FOR OPTIMAL MULTITHREADING PERFOMRANCE
          DOUBLE PRECISION :: TC_FACTOR   ! TIME CONVERSION FACTOR: 1 FOR SECONDS, 1/60 FOR MINUTES; 1/86400 FOR DAYS
          DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: REP_VOL ! REPRESENTATIVE VOLUME : KEEP IT IN THE MAGNITUDE OF 0.01 TO 100 LITRES i.e. ONE TO TWO ORDER OF MAGNITUDES
          DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: POROSITY ! POROSITY
          DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: SATURATION ! SATURATION: VOLUME OF PORES FILLED WITH WATER / VOLUME OF PORES
          DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: TEMP ! TEMPERATURE ARRAY
          DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: DENSITY  ! IF DENSITY DEPENDENT FLOWS ARE IMPLEMENTED IN THE FUTURE
          DOUBLE PRECISION, DIMENSION(:), ALLOCATABLE :: PRESSURE  ! PRESSURE = HEAD*DENSITY*ACC_DUE_TO_GRAVITY        (H*pho*g)
          CHARACTER(LEN = 1):: C
          CHARACTER(LEN = 100):: LINE
          CHARACTER(LEN = 100):: DUMMY_CHAR
          
          ALLOCATE(REP_VOL(NUMNP))
          ALLOCATE(POROSITY(NUMNP))
          ALLOCATE(SATURATION(NUMNP))
          ALLOCATE(TEMP(NUMNP))
          ALLOCATE(DENSITY(NUMNP))
          ALLOCATE(PRESSURE(NUMNP))
                    
          WRITE(*,*) "NUMNP = ", NUMNP    ! = NXYZ IN DOCUMENTATION 
          
          !!!!! SETTING VALUES OF FLUID AND MATRIX PROPERTIES
          !!!!! TAKING SOME STANDARD VALUE (1 kg/l) FOR DENSITY. MAY BE CHANGED ONCE DENSITY DEPENDENCE IS BUILT IN                   
          DO I = 1, NUMNP
              SATURATION(I) = ThNew(I)
              M = MatNumN(I)
              POROSITY(I) = ThTot(M)
              REP_VOL(I) = (nodeArea(I)*1.0)*0.001   ! ADOPTING 1 cm WIDTH AND SUBSEQUENT CONVERTED TO LITRES. WARNING: REP_VOL WITHIN 1 TO 2 MAGNITUDES OF A LITRE
              TEMP(I) = Tmpr(I)    ! IN DEGREE CELSIUS BY DEFAULT
              DENSITY(I) = 1.0  ! IN Kg/Litre
              PRESSURE(I) = (hNew(I)*DENSITY(I)*9.81)/(101325) ! IN ATMOSPHERE (atm): 1 atm = 101325 Pascals:    THE PRESSURE COMPUTED BY H*Pho*g IS IN Pa 
          END DO
          
          N_THREADS = 1
          LB_FACTOR = 0.5
          
          PHREEQCRM_ID = RM_Create(NUMNP,100)   ! CREATING AN INSTANCE OF PHREEQC_RM: ID AND NUMBER OF THREADS
          WRITE(*,*) "PHREEQCRM_ID = ", PHREEQCRM_ID
          
          !   SET PROPERTIES          
          STATUS = RM_SetErrorOn(PHREEQCRM_ID,1)
          STATUS = RM_SetErrorHandlerMode(PHREEQCRM_ID, 1)    ! 0 TO RETURN, 1 TO THROW EXCEPTION AND 2 TO EXIT 
          STATUS = RM_SetComponentH2O(PHREEQCRM_ID, 1)    ! TO INCLUDE WATER AS A COMPONENT INSTEAD OF TOTAL H AND TOTAL O
          STATUS = RM_SetRebalanceFraction(PHREEQCRM_ID, LB_FACTOR)     ! FOR BALANCING LOAD TO GET OPTIMUM PERFORMANCE IN MULTITHREADING
          STATUS = RM_SetRebalanceByCell(PHREEQCRM_ID, 1)     ! 0 FOR AVERAGE TIMES AND 1 FOR INDIVIDUAL CELLS
          STATUS = RM_UseSolutionDensityVolume(PHREEQCRM_ID, 0)   ! 0 FOR CALCULATION OF VOLUME AND DENSITY USING REP_VOL, SETDENSITY AND SETVOLUME OPTIONS, 1 FOR CALCULATION OF DENSITY BY PHREEQCRM
          STATUS = RM_SetPartitionUZSolids(PHREEQCRM_ID, 1)   ! 1 IF UNSATURATION IS INVOLVED. IT SCALES THE REACTIONS AS PER SATURATION
          
          !   SET CONCENTRATION UNITS FOR DIFFERENT MODES OF REACTIONS LIKE SOLUTION, KINETICS, EXCHANGE, SURFACE ETC..           
          STATUS = RM_SetUnitsSolution(PHREEQCRM_ID, 2)   ! 1 FOR mg/L ; 2 FOR mol/L and 3 FOR kg/Kg
          STATUS = RM_SetUnitsPPassemblage(PHREEQCRM_ID, 1)  ! 0 FOR mol/L cell ; 1 FOR mol/L water and 2 FOR mol/L ROCK
          STATUS = RM_SetUnitsExchange(PHREEQCRM_ID, 1)   ! 0 FOR mol/L cell ; 1 FOR mol/L water and 2 FOR mol/L ROCK
          STATUS = RM_SetUnitsSurface(PHREEQCRM_ID, 1)    ! 0 FOR mol/L cell ; 1 FOR mol/L water and 2 FOR mol/L ROCK
          STATUS = RM_SetUnitsGasPhase(PHREEQCRM_ID, 1)   ! 0 FOR mol/L cell ; 1 FOR mol/L water and 2 FOR mol/L ROCK
          STATUS = RM_SetUnitsSSassemblage(PHREEQCRM_ID, 1)   ! 0 FOR mol/L cell ; 1 FOR mol/L water and 2 FOR mol/L ROCK
          STATUS = RM_SetUnitsKinetics(PHREEQCRM_ID, 1)   ! 0 FOR mol/L cell ; 1 FOR mol/L water and 2 FOR mol/L ROCK
          
          !   SET UNITS FOR TIME :: KEEPING DEFAULT UNITS OF SECONDS THAT IS USED BY PHREEQC
          !TC_FACTOR = 1.0
          !STATUS =RM_SetTimeConversion(PHREEQCRM_ID,DBLE(1.0/TC_FACTOR))
          
          !   SET SATURATION, POROSITY ,REP_VOL AND TEMPERATURE : PASSES ARRAYS TO PHREEQCRM
          STATUS = RM_SetSaturationUser(PHREEQCRM_ID, SATURATION)
          STATUS = RM_SetPorosity(PHREEQCRM_ID,POROSITY)
          STATUS = RM_SetRepresentativeVolume(PHREEQCRM_ID,REP_VOL)
          STATUS = RM_SetTemperature(PHREEQCRM_ID,TEMP)
          STATUS = RM_SetDensity(PHREEQCRM_ID,DENSITY)	
          STATUS = RM_SetPressure(PHREEQCRM_ID,PRESSURE)
          WRITE(*,*) "STATUS  = " ,STATUS
          
          !   SET AND OPEN FILES
          STATUS = RM_SetFilePrefix(PHREEQCRM_ID, "PHREEQCRM_2DSOIL") ! PREFIX FOR CHEM FILE
          STATUS = RM_OpenFiles(PHREEQCRM_ID)
          
          !   SET PRINTING CONDITIONS
          STATUS = RM_SetPrintChemistryOn(PHREEQCRM_ID, 0, 1, 0) ! ORDER: WORKERS, INITIAL PHREEQCRM, UTILITY
          STATUS = RM_SetSpeciesSaveOn(PHREEQCRM_ID, 1)   !   A PREREQUISITE FOR GET SPECIES CONCENTRATIONS
          
          !   SET DATABASE
!          STATUS = RM_LoadDatabase(PHREEQCRM_ID, "phreeqc.dat")   ! OTHER OPTIONS ARE THERE TOO
          STATUS = RM_LoadDatabase(PHREEQCRM_ID, "Amm_2DSOIL.dat")          
          
          !   EXAMPLE OF FILE JUST FOR SHOWCASING CAPABILTIES. IT IS RECOMMENDED TO WRITE YOUR OWN FILE
          !   WRITE TO THE PHREEQCRM RUN FILE          
          !OPEN(UNIT = 1001, FILE = 'PHREEQCRM_RUNFILE.pqi',STATUS = 'UNKNOWN')          
          !    WRITE (1001,*) 'TITLE   NITRATE PROBLEM'
          !    WRITE (1001,*) 'SOLUTION   0'
          !    WRITE (1001,*) 'units   mg/l'
          !    WRITE (1001,*) 'pH    7.0 '
          !    WRITE (1001,*) 'pe    13.0  O2(g) -0.68'
          !    WRITE (1001,*) 'SAVE SOLUTION 0'
          !    WRITE (1001,*) 'END'  
          !    
          !    WRITE (1001,*) 'SOLUTION 1-600'
          !    WRITE (1001,*) 'units   mg/l'
          !    WRITE (1001,*) 'pH    7.0 '
          !    WRITE (1001,*) 'Amm  ',1e-5,' charge'
          !    WRITE (1001,*) 'N  ',1e-5
          !    WRITE (1001,*) 'USE SOLUTION 0'
          !    WRITE (1001,*) 'END'
          !    
          !    WRITE (1001,*) 'SELECTED_OUTPUT'
          !    WRITE (1001,*) '    -active     true'
          !    WRITE (1001,*) '    -reset     false'
          !    WRITE (1001,*) '    -file   PHREEQCRM_SELECTED_OUTPUT.sel'
          !    WRITE (1001,*) '    -pH     true'
          !    WRITE (1001,*) '    -pe     true'
