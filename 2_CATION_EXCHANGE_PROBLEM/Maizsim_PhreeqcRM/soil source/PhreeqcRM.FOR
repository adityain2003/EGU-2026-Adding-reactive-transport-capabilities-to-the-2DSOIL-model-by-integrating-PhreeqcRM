          !    WRITE (1001,*) '-iterations 2000'
          !    WRITE (1001,*) '-tolerance 1e-20'          
          !CLOSE(1001)
          
          !   RUN PHREEQC INPUT FILE TO SET THE INITIAL CONDITIONS
          
           STATUS = RM_RunFile(PHREEQCRM_ID,0,1,0,"PHREEQCRM_RUNFILE_CATION_EXCHANGE.pqi")    ! ID, WORKERS, INITIAL_PHREEQC, UTILITY
           DUMMY_CHAR = "DELETE; -all"
           STATUS = RM_RunString(PHREEQCRM_ID,1,0,1,DUMMY_CHAR)   ! CLEAR DATA FROM WORKERS AND UTILITY TO PREVENT STRAY DATA
          
      END SUBROUTINE PHREEQC_RM_INITIALIZE