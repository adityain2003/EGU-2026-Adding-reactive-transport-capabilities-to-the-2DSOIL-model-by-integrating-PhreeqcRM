! AK: SUBROUTINE FOR IMPLEMENTING FERTIGATION OPERATIONS
      ! SECTION 1: READING INPUT FILE FOR INGESTION OF FERTIGATION SCHEDULE
      ! SECTION 2: SETTING UP THE TIMING OF FERTIGATION OPERATIONS
      ! SECTION 3: COMMENCEMENT OF FERTIGATION OPERATIONS
      
      ! NOTES: 1) THIS SUBROUTINE CAN ALSO IMPLEMENT HEAD AT SURFACE BOUNDARY, JUST LIKE PONDED IRRIGATION MODULE. THUS, THIS SUBROUTINE CAN BE CONSIDERED AS ANOTHER OPTION
      !        2) THERE MAY BE POSSIBLE COMNFLICTS WITH IRRIGATION MODULES SUCH AS PONDED BY HEAD OR PONDED BY FLUX OR OTHERS. BETTER TO KEEP THEM OFF
      
      SUBROUTINE FERTIGATION
      
      INCLUDE 'public.ins'
      
      INTEGER:: I,J,K
      INTEGER:: DAY_LOCAL,MONTH_LOCAL,YEAR_LOCAL
      DOUBLE PRECISION:: TIME_LOCAL
      INTEGER,PARAMETER:: MAX_FERT_TIMES = 100  ! MAXIMUM NUMBER OF FERTILIZATION EVENTS
      INTEGER:: NUM_FERT_EVENTS, FERTS_APPLIED ! NUMBER OF FERTILIZATION EVENTS
      INTEGER:: ModNum
      INTEGER:: FERTIGATION_STARTED   ! A FLAG IMPLYING CONTINUATION OF THE FERTIGATION EVENT
      INTEGER:: CodeS_BC_SURF_INPUT, CodeW_BC_SURF_INPUT
      
      CHARACTER(LEN=10) :: DATE_LOCAL,TEMP_DATE
      CHARACTER(LEN=10) :: START_DATE(MAX_FERT_TIMES), END_DATE(MAX_FERT_TIMES) ! START AND END DATE FOR FERTIGATION
      DOUBLE PRECISION :: HOUR_LOCAL ! LOCAL HOUR
      INTEGER :: START_HOUR(MAX_FERT_TIMES), END_HOUR(MAX_FERT_TIMES) ! START AND END HOUR FOR FERTIGATION
      DOUBLE PRECISION :: FERT_START_TIME(MAX_FERT_TIMES),FERT_STOP_TIME(MAX_FERT_TIMES)
      DOUBLE PRECISION :: FERTIGATION_S_VALUE(MAX_FERT_TIMES), ! FERTILIZATION SOLUTE INFLUX RATE IN microgram/Day/cm
     -                    FERTIGATION_W_VALUE(MAX_FERT_TIMES) ! FERTILIZATION WATER INFLUX RATE IN cm/day (confused with cm^3/Day/cm)
      
      COMMON/FERTIGATION_CB/START_DATE,START_HOUR,END_DATE,END_HOUR,
     - FERTIGATION_S_VALUE,FERTIGATION_W_VALUE,FERTS_APPLIED,NUM_FERT_EVENTS,FERTIGATION_STARTED,
     - CodeS_BC_SURF_INPUT,CodeW_BC_SURF_INPUT,ModNum
      
      !!!!! SECTION 1: READING INPUT FILE FOR INGESTION OF FERTIGATION SCHEDULE !!!!!
      ! FORMAT OF INPUT FILE:
      ! LINE 1: FILE DESCRIPTION
      ! LINE 2: DESCRIPTION OF VARIABLE: NUM_FERT_EVENTS
      ! LINE 3: NUMBER OF FERTIGATION EVENTS (NUM_FERT_EVENTS)
      ! LINE 4 TO 3+NUM_FERT_EVENTS : START DAY, START HOUR, END DAY, END HOUR, SOLUTE FLUX, WATER FLUX
          
      IF (lInput == 1) THEN
          
          NUM_FERT_EVENTS = 0
          FERTS_APPLIED = 0
          FERTIGATION_STARTED = 0
          
          NumMod = NumMod + 1
          ModNum = NumMod
          
          OPEN(UNIT = 11, NAME='FERTIGATION_SCHEDULE.txt', STATUS = 'OLD')
          READ(11,*) ! NOT TO BE READ :  LINE WITH TITLE AND DESCRIPTION
          READ(11,*) ! DESCRIPTION
          READ(11,*) NUM_FERT_EVENTS
          READ(11,*) ! DESCRIPTION OF BC TYPE
          READ(11,*) CodeS_BC_SURF_INPUT, CodeW_BC_SURF_INPUT ! CodeS = +3 FOR PRESCRIBED CONCENTRATION (DIRICHLET BC/FIRST TYPE OF BC). CodeS =- 4 FOR NEUMAN BCs (PRESCRIBED FLUX)
          READ(11,*) ! DESCRIPTION OF FERTIGATION DATA AND PRESCRIBED BC VALUES
          
          IF (NUM_FERT_EVENTS.EQ.0) THEN
              tNext(ModNum) = 1e22 ! IF THERE ARE NO FERTIGATION EVENTS, ASSIGN A INVOKATION TIME TO A VERY LARGE VALUE  
          END IF
          
          ! ORDER OF DATA ENTRY: 1)START_DATE 2)START_HOUR 3)END_DATE 4)END_HOUR 5)SOLUTE_BC_VALUE 6)WATER_BC_VALUE
          IF (NUM_FERT_EVENTS.NE.0) THEN
              DO I=1,NUM_FERT_EVENTS
                  READ(11,*) START_DATE(I), START_HOUR(I), END_DATE(I), END_HOUR(I),  FERTIGATION_S_VALUE(I), FERTIGATION_W_VALUE(I)  
              END DO

          DO I=1,NUM_FERT_EVENTS
              FERT_START_TIME(I) = JULDAY(START_DATE(I)) + START_HOUR(I)/24.0
              FERT_STOP_TIME(I) = JULDAY(END_DATE(I)) + END_HOUR(I)/24.0
          END DO         
          
          tNext(ModNum) = FERT_START_TIME(1)
          
          FERTS_APPLIED = 0   
          
          OPEN(UNIT = 11, NAME='FERTIGATION_OUTPUT.txt', STATUS = 'UNKNOWN')
          WRITE(11,*) 'NODE_NUM','  ','TIME','  ','DATE',' HOUR ','  ','X','  ','Y','  ','CONC'
          END IF
          
      END IF ! END OF linput = 1 CONDITION  
            
      ! SECTION 3: COMMENCEMENT OF FERTIGATION EVENTS

      IF(ABS(Time-tNext(ModNum)).lt.0.001*Step) THEN  ! THIS CONDITION IS INVOKED WHEN EITHER STARTING OR STOPPING THE FERTIGATION
          IF (FERTIGATION_STARTED == 0) THEN  ! FERTIGATION STARTING
              FERTIGATION_STARTED = 1
              FERTS_APPLIED = FERTS_APPLIED+1 
              J = FERTS_APPLIED
              tNext(ModNum) = FERT_STOP_TIME(FERTS_APPLIED)              
          ELSE IF (FERTIGATION_STARTED == 1) THEN ! STOPPING FERTIGATION STOPPING
              FERTIGATION_STARTED = 0
              ! DECISION TO START THE NEXT ITERATION OF FERTIGATION
              IF (FERTS_APPLIED.LT.NUM_FERT_EVENTS) THEN
                  tNext(ModNum) = FERT_START_TIME(FERTS_APPLIED+1)
              ELSE IF (FERTS_APPLIED.LT.NUM_FERT_EVENTS) THEN
                  tNext(ModNum) = 1e22
              END IF
          END IF
      END IF         
              
          
      IF (FERTIGATION_STARTED == 1) THEN
          DO I=1,nSurf                     
                  CodeS(I) = CodeS_BC_SURF_INPUT                   
                  VarBS(I,1) = FERTIGATION_S_VALUE(FERTS_APPLIED)
                  IF (CodeW_BC_SURF_INPUT == -4) THEN 
                      CodeW(I) = -4       !  CODE(W?S?H?G) =- 4 FOR NEUMAN BCs (PRESCRIBED FLUX). CODE(W/S/H/G) = +4 FOR PRESCRIBED HEAD/CONC/TEMP/GAS CONTENT (DIRICHLET BC/FIRST TYPE OF BC).
                      Q(I) = Q(I) + FERTIGATION_W_VALUE(FERTS_APPLIED)*width(I)  ! FORMULATION CHECKED IWTH DENNIS: Q IS MULTIPLIED BY STEP IN WATER MOVER
                  ELSE IF (CodeW_BC_SURF_INPUT == 1) THEN ! FIRST TYPE OF BC (DIRICHLET BC)
                      CodeW(I) = 1
                      hNew(I) = FERTIGATION_W_VALUE(FERTS_APPLIED)
                  END IF                      
          END DO                
      END IF ! END OF I(FERTIGATION_STARTED == 1)
      
      WRITE(*,*) 'TIME=',Time
      
      CALL caldat(INT(AINT(Time)),MONTH_LOCAL, DAY_LOCAL, YEAR_LOCAL)
      WRITE(TEMP_DATE,'(I2.2,A1,I2.2,A1,I4)') MONTH_LOCAL,'/',DAY_LOCAL,'/',YEAR_LOCAL 
      DATE_LOCAL = TEMP_DATE
      HOUR_LOCAL = 24*(Time-(AINT(Time)))
            
      IF (HOUR_LOCAL==0.0) THEN ! ONLY WRITE AT THE BEGINNING OF HOURS
      ! IF (INT(HOUR_LOCAL)==0.0)
          
          DO I = 1, NUMNP
              IF (x(I)==0.0) THEN ! ONLY WRITE IF NEW DAY IS STARTING AND X = 0 (1-D PROFILE)
!              WRITE (11,'(I4, 1X, F9.2, 1X, A10, 1X, F5.2, 1X, F8.2, 1X, F8.2, 1X, F10.4 )') 
              WRITE(11,'(*(G0.5," ,"))')
     -        I, Time, DATE_LOCAL, HOUR_LOCAL, x(I), y(I), Conc(I,1)
!         11 FORTMAT (I4, F9.2, A10, F4.2, F8.2, F8.2, F8.2 )
      !   I4: NOD_NUM, F9.2: Time, A10: DATE_LOCAL, F4.2: HOUR_LOCAL, F8.2: x, F8.2: y, F8.2: Conc  
              END IF
          END DO
      
      END IF ! END OF WRITING CONDITION
      
      RETURN
      END SUBROUTINE FERTIGATION